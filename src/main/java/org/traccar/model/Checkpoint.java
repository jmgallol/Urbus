package org.traccar.model;

import org.traccar.storage.StorageName;

/**
 * Checkpoint model for bus stop/paradero monitoring.
 * A checkpoint is a specific location where buses arrive.
 * Extends GroupedModel to inherit permission system.
 */
@StorageName("tc_checkpoints")
public class Checkpoint extends GroupedModel {

    private String name;
    private String description;
    private double latitude;
    private double longitude;
    private int radius; // Proximity radius in meters

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public double getLatitude() {
        return latitude;
    }

    public void setLatitude(double latitude) {
        this.latitude = latitude;
    }

    public double getLongitude() {
        return longitude;
    }

    public void setLongitude(double longitude) {
        this.longitude = longitude;
    }

    public int getRadius() {
        return radius;
    }

    public void setRadius(int radius) {
        this.radius = radius;
    }
}
